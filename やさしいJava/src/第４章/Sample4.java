package 第４章;

public class Sample4 {
	public static void main(String[] args)
	{
		int num1 = 10;
		int num2 = 5;
		System.out.println("num1とnum2にいろいろな演算を行います。");
		System.out.println("num1+num2は" + (num1+num2) + "です。");//たし算
		System.out.println("num1-num2は" + (num1-num2) + "です。");//ひき算
		System.out.println("num1*num2は" + (num1*num2) + "です。");//かけ算
		System.out.println("num1/num2は" + (num1/num2) + "です。");//わり算
		System.out.println("num1%num2は"+ (num1%num2) + "です。");//あまり
	}
}
