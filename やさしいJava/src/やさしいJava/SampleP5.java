package やさしいJava;
/*次のように画面に出力するコードを記述してください。８進数・１６進数を使って、２とおりのコードを記述してください。
 6
 20
 13
*/
public class SampleP5 {
	public static void main(String[] args){
		System.out.println("８進数の６は"+ 06 +"です。");
		System.out.println("１６進数の６は"+ 0x6 +"です。");
		System.out.println("８進数の２０は"+ 024 +"です。");
		System.out.println("１６進数の２０は"+ 0x14 +"です。");
		System.out.println("８進数の１３は"+ 015 +"です。");
		System.out.println("１６進数の１３は"+ 0xD +"です。");
	}
}
